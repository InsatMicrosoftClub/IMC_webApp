    // This is useful if you do not want to tear down the database each time you run the application.
    // public class ApplicationDbInitializer : DropCreateDatabaseAlways<ApplicationDbContext>
    // This example shows you how to create a new database if the Model changes
    public class ApplicationDbInitializer : DropCreateDatabaseIfModelChanges<ApplicationDbContext> 
    {
        protected override void Seed(ApplicationDbContext context) {
            InitializeIdentityForEF(context);
            base.Seed(context);
        }

        //Create User=admin@123.Mm with password=123.Mm in the Admin role        
        public static void InitializeIdentityForEF(ApplicationDbContext db) {
            var userManager = HttpContext.Current.GetOwinContext().GetUserManager<ApplicationUserManager>();
            var roleManager = HttpContext.Current.GetOwinContext().Get<ApplicationRoleManager>();
            const string name = "admin@123.Mm";
            const string password = "123.Mm";
            const string roleName_Admin = "Admin";
            const string roleName_User = "User";

            //Create Role Admin if it does not exist
            var role_Admin = roleManager.FindByName(roleName_Admin);
            if (role_Admin == null)
            {
                role_Admin = new ApplicationRole(roleName_Admin);
                var roleresult = roleManager.Create(role_Admin);
            }
            //Create Role User if it does not exist
            var role_User = roleManager.FindByName(roleName_User);
            if (role_User == null)
            {
                role_User = new ApplicationRole(roleName_User);
                var roleresult = roleManager.Create(role_User);
            }

            // Create user admin if not already existant
            var user_admin = userManager.FindByName(name);
            if (user_admin == null) {
                user_admin = new ApplicationUser { UserName = name, Email = name };
                var result = userManager.Create(user_admin, password);
                result = userManager.SetLockoutEnabled(user_admin.Id, false);
            }

            var rolesForUser = userManager.GetRoles(user_admin.Id);
            // Add user admin to Role Admin if not already added
            if (!rolesForUser.Contains(role_Admin.Name))
            {
                var result = userManager.AddToRole(user_admin.Id, role_Admin.Name);
            }
            // Add user admin to Role User if not already added
            if (!rolesForUser.Contains(role_User.Name))
            {
                var result = userManager.AddToRole(user_admin.Id, role_User.Name);
            }

            // Initialize database immediately
            db.Database.Initialize(false);
        }
    }

    public class ApplicationSignInManager : SignInManager<ApplicationUser, string>
    {
        public ApplicationSignInManager(ApplicationUserManager userManager, IAuthenticationManager authenticationManager) : 
            base(userManager, authenticationManager) { }

        public override Task<ClaimsIdentity> CreateUserIdentityAsync(ApplicationUser user)
        {
            return user.GenerateUserIdentityAsync((ApplicationUserManager)UserManager);
        }

        public static ApplicationSignInManager Create(IdentityFactoryOptions<ApplicationSignInManager> options, IOwinContext context)
        {
            return new ApplicationSignInManager(context.GetUserManager<ApplicationUserManager>(), context.Authentication);
        }
    }
